package csse2002.block.world;

/**
 * An exception that indicates that an action is formatted incorrectly.
 */
public class ActionFormatException
    extends Exception {

    /**
     *
     */
    public ActionFormatException() {
        super();
    }

    /**
     *
     * @param message
     */
    public ActionFormatException(java.lang.String message) {
        super(message);
    }
}
